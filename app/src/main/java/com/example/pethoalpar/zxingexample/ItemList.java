package com.example.pethoalpar.zxingexample;
public class ItemList {

    private String name;
    private int price;

    public ItemList(String name, int price) {
        this.name = name;
        this.price = price;

    }

    public String getName() {
        return name;
    }


    public int getPrice() {
        return price;
    }

}
